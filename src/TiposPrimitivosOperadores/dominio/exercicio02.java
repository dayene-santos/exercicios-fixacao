package TiposPrimitivosOperadores.dominio;

public class exercicio02 {

    /**
     * | Tipo Primitivo | Tamanho em Memória | Descrição |
     * |----------------|--------------------|-----------|
     * |    byte        | 1 byte             | Tipo de dado numérico para números inteiros pequenos. Intervalo: -128 a 127. |
     * |    short       | 2 bytes            | Tipo de dado numérico para números inteiros. Intervalo: -32,768 a 32,767. |
     * |    int         | 4 bytes            | Tipo de dado numérico para números inteiros. Intervalo: -2^31 a 2^31-1. |
     * |    long        | 8 bytes            | Tipo de dado numérico para números inteiros grandes. Intervalo: -2^63 a 2^63-1. |
     * |    float       | 4 bytes            | Tipo de dado numérico para números de ponto flutuante com precisão simples. Intervalo: Aproximadamente ±1.4 × 10^−45 a ±3.4 × 10^38. |
     * |    double      | 8 bytes            | Tipo de dado numérico para números de ponto flutuante com precisão dupla. Intervalo: Aproximadamente ±4.9 × 10^−324 a ±1.8 × 10^308. |
     * |    char        | 2 bytes            | Tipo de dado para armazenar um único caractere Unicode. Intervalo: 0 a 65,535. |
     * |    boolean     | 1 byte (virtualmente) | Tipo de dado lógico que armazena valores `true` ou `false`. O tamanho real pode variar, mas é geralmente tratado como 1 byte pela JVM. |
     *
     * Resumo:
     * - byte    -> ideal para armazenar valores inteiros pequenos.
     * - short   -> para inteiros médios.
     * - int     -> o tipo de inteiro mais comum.
     * - long    -> para inteiros grandes.
     * - float   -> para números decimais de precisão simples.
     * - double  -> para números decimais de precisão dupla.
     * - char    -> para armazenar caracteres Unicode.
     * - boolean -> para valores lógicos `true` ou `false`.
     *
     * Esses tamanhos são fixos e não variam dependendo da plataforma, já que Java é uma linguagem de alto nível com tipagem estática e uma Máquina Virtual (JVM) que garante a consistência do comportamento dos tipos primitivos.
     */
}
