package TiposPrimitivosOperadores.teste;

public class exercicio05 {
    public static void main(String[] args) {

        int x = 10;
        int y = 5;

        int soma, subtracao, multiplicacao, divisao;

        soma = x + y;
        subtracao = x - y;
        multiplicacao = x * y;
        divisao = x / y;

        System.out.println("A soma dos valores é: " + soma);
        System.out.println("A diferença dos valores é: " + subtracao);
        System.out.println("O produto dos valores é: " + multiplicacao);
        System.out.println("A divisão dos valores é: " + divisao);

    }
}
